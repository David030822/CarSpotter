class OwnCar {
  String name;
  String fuelType;
  int kilometers;
  int year;
  double price;
  String chassis;
  String gearbox;
  int engineSize;
  int horsepower;
  double buyPrice;
  double spent;
  double sellPrice;
  String imagePath;

  OwnCar({
    required this.name,
    required this.fuelType,
    required this.kilometers,
    required this.year,
    required this.price,
    required this.chassis,
    required this.gearbox,
    required this.engineSize,
    required this.horsepower,
    required this.buyPrice,
    required this.spent,
    required this.sellPrice,
    required this.imagePath
  });
}